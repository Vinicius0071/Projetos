    public void readJTable() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTViagem.getModel();
        modelo.setNumRows(0);
        ViagemDAO vdao = new ViagemDAO();

        for (Viagem v : vdao.read()) {
            modelo.addRow(new Object[]{
                v.getIDViagem(),
                v.getNomeCliente(),
                v.getOrigem(),
                v.getDestino()

            });

        }
    }

